# # Explain Exception handling? What is an Error in Python?

# Exception handling is a way to manage errors during program execution using:

# try block: Code that might cause an exception.

# except block: Code that runs if an exception occurs.

# else block (optional): Runs if no exception occurs.

# finally block (optional): Always runs (used for cleanup).


# An error is a problem in the code that stops the execution of the program.