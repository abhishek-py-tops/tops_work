# # OOPs (Object-Oriented Programming System) is a programming paradigm based on the concept of "objects". It organizes code into reusable and modular structures.
# | Concept              | Description                                                               |
# | -------------------- | ------------------------------------------------------------------------- |
# | **1. Class**         | Blueprint for creating objects (e.g., `Student`, `Car`)                   |
# | **2. Object**        | Instance of a class containing data and behavior                          |
# | **3. Inheritance**   | Allows one class to acquire properties of another (reuse code)            |
# | **4. Polymorphism**  | Same method behaves differently in different contexts                     |
# | **5. Encapsulation** | Wrapping data and methods into a single unit (class) and hiding internals |
# | **6. Abstraction**   | Hiding complex implementation details and showing only essential features |


